    const stage = document.getElementById('stage');
    const modal = document.getElementById('modal');
    const galleryGrid = document.getElementById('galleryGrid');
    const btnViewAll = document.getElementById('btnViewAll');
    const btnClose = document.getElementById('btnClose');
    const pathTip = document.getElementById('pathTip');

    let currentIndex = 0;
    let timer = null;
    const intervalMs = 2800; // 在这里调整明信片出现间隔，数值越小出现越快

    function createImageBlock(data) {
      const wrap = document.createElement('div');
      wrap.className = 'postcard-img-container';
      wrap.innerHTML = `
        <div class="placeholder"><div class="icon">✉</div><div>图片未加载，请检查 images 文件夹</div></div>
        <img src="${data.image}" alt="儿童祝福明信片 ${data.number || ''}" loading="eager">
      `;
      const img = wrap.querySelector('img');
      img.addEventListener('error', () => {
        img.style.display = 'none';
        pathTip.style.display = 'block';
      });
      img.addEventListener('load', () => {
        img.style.display = 'block';
        // 自动判断横版/竖版：竖版明信片完整显示，不再被裁切
        wrap.classList.remove('portrait-image', 'landscape-image');
        if (img.naturalHeight > img.naturalWidth * 1.08) {
          wrap.classList.add('portrait-image');
        } else {
          wrap.classList.add('landscape-image');
        }
      });
      return wrap;
    }

    function createPostcard(data, gallery = false) {
      const card = document.createElement('article');
      card.className = gallery ? 'gallery-card' : 'postcard';
      const rot = ((Math.random() * 6) - 3).toFixed(2) + 'deg';
      card.style.setProperty('--rot', rot);
      card.appendChild(createImageBlock(data));

      const caption = document.createElement('div');
      caption.className = 'caption-line';
      caption.innerHTML = `<span>FROM：${data.from || '孩子们'}</span><span class="stamp">${data.stamp || 'LOVE'}</span>`;
      card.appendChild(caption);
      return card;
    }

    function showNextPostcard() {
      if (!postcards.length) return;
      const data = postcards[currentIndex];
      const card = createPostcard(data, false);
      stage.appendChild(card);
      setTimeout(() => {
        if (card && card.parentNode) card.parentNode.removeChild(card);
      }, 6700);
      currentIndex = (currentIndex + 1) % postcards.length;
    }

    function startLoop() {
      stopLoop();
      showNextPostcard();
      timer = setInterval(showNextPostcard, intervalMs);
    }

    function stopLoop() {
      if (timer) clearInterval(timer);
      timer = null;
    }

    function openGallery() {
      stopLoop();
      galleryGrid.innerHTML = '';
      postcards.forEach((data) => galleryGrid.appendChild(createPostcard(data, true)));
      modal.classList.add('active');
    }

    function closeGallery() {
      modal.classList.remove('active');
      startLoop();
    }

    btnViewAll.addEventListener('click', openGallery);
    btnClose.addEventListener('click', closeGallery);
    modal.addEventListener('click', (event) => {
      if (event.target === modal) closeGallery();
    });
    document.addEventListener('keydown', (event) => {
      if (event.key === 'Escape' && modal.classList.contains('active')) closeGallery();
    });

    document.addEventListener('DOMContentLoaded', startLoop);
  
